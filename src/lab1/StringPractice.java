package lab1;

class StringPractice
{
    public static void main(String[] args)
    {
        String helloWorld = "Hello World";
        // Camel-casing variable names
        // Beging with lower case and use upper case for
        // the following word. thisIsAnExample 
        System.out.println(helloWorld);
        
        // Modulus argument %
        
        int a = 11 % 5;
        
        System.out.println(a);
        
        String hello = "Hello ";
        String world = "World!";
        String helloWorlds = hello + world;
        
        // Strings are concatinated using the + symbol
        
        System.out.println(helloWorlds);
      
    }
}